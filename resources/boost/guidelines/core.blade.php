### Webhooks (Incoming Events from NGO.Tools)

NGO.Tools sends webhooks to this app when things happen in the platform. The app **receives and reacts** to these events ‚Äî it does not trigger them.

Available events: `contact.created`, `contact.updated`, `contact.deleted`, `donation.created`

- Webhook handler: `routes/ngotools-webhooks.php`
- Signature header: `X-NGOTools-Signature` (HMAC-SHA256)
- Timestamp header: `X-NGOTools-Timestamp` (unix seconds)
- Event header: `X-NGOTools-Event` (e.g. `contact.created`)
- Signature formula: `hash_hmac('sha256', timestamp . '.' . rawBody, secret)`
- Replay protection: ¬±300 seconds tolerance
- Use `VerifyWebhookSignature` middleware on webhook routes

### NGO.Tools REST API (Outgoing Requests)

To read or write data in NGO.Tools, use the API with the token from the bridge state:

```php
Http::withToken($accessToken)
    ->baseUrl($apiBaseUrl)
    ->get('/contacts');
```

This app does NOT have its own contacts/donations/members tables. It accesses NGO.Tools data exclusively through the API.

### Important Rules

- This app is an **extension** ‚Äî it does not own contacts, donations, or other NGO.Tools domain data
- NEVER publish or override `ngotools::layouts.app` ‚Äî it stays in the package and auto-updates
- Views live in `resources/views/ngotools/pages/` (app views), NOT in `resources/views/vendor/`
- `AllowIframeEmbedding` middleware is automatically applied to all UI routes by the service provider
- When making API calls to NGO.Tools, use `state.apiBaseUrl` with `Authorization: Bearer {state.accessToken}`
- The `.env` keys are: `NGOTOOLS_API_URL`, `NGOTOOLS_DEV_TOKEN`, `NGOTOOLS_WEBHOOK_SECRET`, `NGOTOOLS_PORT`
