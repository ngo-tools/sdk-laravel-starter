## NGO.Tools App SDK

This application is an **extension** (third-party app) for the NGO.Tools platform, built with `ngo-tools/sdk-laravel-starter`.

### Mental Model ‚Äî Read This First

This app is a **plugin** that extends NGO.Tools. It does NOT own domain data like contacts, donations, or projects ‚Äî those live in NGO.Tools. This app can:

1. **Display UI** inside NGO.Tools via iframe (UI slots)
2. **React to events** from NGO.Tools via webhooks (e.g. `contact.created`, `donation.created`)
3. **Read/write data** in NGO.Tools via its REST API using a Bearer token

When a user says "when a contact is created", they mean: **when NGO.Tools sends a `contact.created` webhook to this app** ‚Äî NOT that this app should create contacts itself. The same applies to donations, members, etc.

**Data flow:** NGO.Tools ‚Üí webhook ‚Üí this app reacts. Or: this app ‚Üí API call ‚Üí reads/writes data in NGO.Tools.

### Architecture

- The app is rendered inside an **iframe** in the NGO.Tools admin panel
- Communication with the parent frame uses a **postMessage bridge** (global `NGOTools` JS object)
- **Webhooks** from NGO.Tools arrive as HMAC-SHA256 signed POST requests at `routes/ngotools-webhooks.php`
- **API access** uses a Bearer token provided through the bridge init state (`state.accessToken`)

### Artisan Commands

- `php artisan ngotools:install` ‚Äî Interactive setup wizard (UI slots, webhooks, scopes)
- `php artisan ngotools:dev` ‚Äî Starts dev server + cloudflared tunnel, registers tunnel with NGO.Tools

### File Structure (app-side, generated by install wizard)

- `routes/ngotools-ui.php` ‚Äî UI routes for iframe pages
- `routes/ngotools-webhooks.php` ‚Äî Webhook handler routes (incoming events from NGO.Tools)
- `resources/views/ngotools/pages/` ‚Äî Blade views for UI slots
- `config/ngotools.php` ‚Äî Configuration (API URL, dev token, webhook secret, port)

### Layout & Bridge

- Views must extend `ngotools::layouts.app` (from the package ‚Äî never publish or override this layout)
- The bridge JS is inlined in the layout to avoid PNA/CORS issues with dev tunnels
- Tailwind CSS is loaded via CDN in the layout ‚Äî no build step needed for simple apps

#### JavaScript Bridge API

```javascript
NGOTools.onInit(callback)       // Register callback for init state (called when iframe loads)
NGOTools.getState()             // Return deep copy of current state
NGOTools.sendNavigate(route)    // Trigger navigation in parent frame
NGOTools.applyTheme(theme)      // Apply 'light' or 'dark' mode
NGOTools.enableDevMode(options) // Simulate init outside iframe (dev only)
```

The `ngotools:init` CustomEvent is dispatched on `document` after initialization:

```javascript
document.addEventListener('ngotools:init', function(e) {
    var state = e.detail;
    // state.user.name, state.tenantId, state.locale, etc.
});
```

#### Bridge State Structure

| Field | Type | Description |
|-------|------|-------------|
| `accessToken` | string | Bearer token for NGO.Tools API |
| `tenantId` | number | Current tenant/organization ID |
| `locale` | string | User locale (`de`, `en`, ...) |
| `theme` | `light` \| `dark` | Current UI theme |
| `primaryColor` | string | Hex color from NGO.Tools theme |
| `apiBaseUrl` | string | Base URL for API requests |
| `user` | object | `{ id, name }` of the logged-in user |
| `context` | object | `{ entityType, entityId }` ‚Äî set on contact tabs etc. |

